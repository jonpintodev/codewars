function makeUpperCase(str) {
  //use the toUpperCase() method
  return str.toUpperCase()
}

console.log(makeUpperCase('Hello'))//'HELLO'
console.log(makeUpperCase('yes'))//'YES'
console.log(makeUpperCase('stop yelling'))//'STOP YELLING'